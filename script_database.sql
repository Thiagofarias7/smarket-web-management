CREATE DATABASE IF NOT EXISTS `smarket`;

USE `smarket`;

CREATE TABLE IF NOT EXISTS `clients` (
    `NUM_PED` int PRIMARY KEY,
    `DATA` date,
    `COD_CLI` int,
    `CLIENTE` varchar(50),
    `ENDERECO` varchar(75),
    `RG` varchar(11),
    `TOTAL_GERAL` float
);

CREATE TABLE IF NOT EXISTS `orders` (
    `NUM_PED` int REFERENCES `clients` (`NUM_PED`),
    `COD_PROD` int,
    `DESCRICAO` varchar(75),
    `QUANT` int,
    `PRECO` float,
    `TOTAL` float,
    `TOTAL_GERAL` float
);

